    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        
        if(this.rutaArchivo.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, " la ruta del fhicero a ordenar no se encuentra definida","CAMPOS EN BLANCO", JOptionPane.WARNING_MESSAGE);
        }else {
            File file = new File(this.rutaArchivo.getText());
            if(!file.isFile()){
                JOptionPane.showMessageDialog(null, "Esta ruta no pertenece a un fichero","EVALUAMOS FICHEROS CSV", JOptionPane.WARNING_MESSAGE);
            }else{
                
                try {
                    
                    Intercalacion miIntercalacion;
                    
                    try {
                        miIntercalacion = new Intercalacion(Integer.parseInt((String) jComboBox2.getSelectedItem()),rutaArchivo.getText(),jComboBox1.getSelectedIndex()+1);
                        miIntercalacion.start();
                    } catch (FileNotFoundException | ParseException ex) {
                        Logger.getLogger(OrdenamientoExterno.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(ventana.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    JOptionPane.showMessageDialog(null, "ARCHIVO ORDENADO\n Â¿Deseas abrir el archivo?","Fin", JOptionPane.YES_NO_OPTION);
                    Desktop.getDesktop().open(file);
                    
                    
                } catch (IOException ex) {
                    Logger.getLogger(ventana.class.getName()).log(Level.SEVERE, null, ex);
                }
                    
                    
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        JFileChooser dlg = new JFileChooser();
        dlg.showOpenDialog(this);
        File abre = dlg.getSelectedFile();
        if(abre!=null){
            
        rutaArchivo.setText(abre.getAbsolutePath());
        
        }else{
            JOptionPane.showMessageDialog(null,"No se ha encontrado el archivo","ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

